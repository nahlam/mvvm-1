import UIKit

@objc public extension UIColor {
    
    public class var appColor: UIColor {
        return UIColor(red: 120 / 255, green: 224 / 255, blue: 195 / 255, alpha: 1.0)
    }
    
    public class var customRedColor50: UIColor {
        return UIColor(red: 255 / 255, green: 0 / 255, blue: 0 / 255, alpha: 0.5)
    }
    
    public class var customGreen: UIColor {
        return UIColor(red: 70 / 255, green: 167 / 255, blue: 93 / 255, alpha: 1.0)
    }
}
